package listaexec2;

/**
 * Faça um programa que gere a seguinte série: 10, 20, 30, 40, ..., 990, 1000.
 * Use o laço FOR.
 *
 * @author Marcelo Pereira
 */
public class exec4 {

    public static void main(String[] args) {
        for (int i = 0; i <= 1000; i = i + 10) {
            System.out.println(i);
        }
    }

}
