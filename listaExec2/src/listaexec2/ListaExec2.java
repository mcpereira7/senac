package listaexec2;

/**
 * Faça um programa que mostre todos os números inteiros de 1 a 50. Use o laço
 * WHILE.
 *
 * @author Marcelo Pereira
 */
public class ListaExec2 {

    public static void main(String[] args) {
        int num = 1;
        while (num <= 50) {
            System.out.println(num);
            num++;
        }
    }

}
